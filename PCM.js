var marks=prompt("Enter your PCM marks :");

if(marks>=95){
    console.log("you will be getting Computer science branch");
}
else if(marks>=90){
    console.log("you will be getting Electronics and Communication branch");
}
else if(marks>=85){
    console.log("you will be getting Mechanical branch");
}
else if(marks>=80){
    console.log("you will be getting Civil branch");
}
else if(marks>=75){
    console.log("you will be getting no branch");
}
else{
    console.log("you will be getting no branch");
}
